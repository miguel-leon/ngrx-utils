import { Action } from '../Action';
import { RegisterDispatcher } from '../RegisterDispatcher';
import { AutogeneratedActionType } from './AutogeneratedActionType.decorator';
import { SerializeOwnPropertySymbols } from '../../util/SerializeOwnPropertySymbols.decorator';


export function ActionClass() {
	return <Args extends any[], T>(target: new(...args: Args) => T): new(...args: Args) => T & Action & RegisterDispatcher => {
		target = AutogeneratedActionType()(target);
		target = SerializeOwnPropertySymbols()(target as any);
		return target as any;
	}
}
